package com.ramirezmatias.mgr.Security.Service;

import com.ramirezmatias.mgr.Security.Repository.IRolRepository;
import com.ramirezmatias.mgr.Securiy.Entity.Rol;
import com.ramirezmatias.mgr.Securiy.Enums.RolNombre;
import java.util.Optional;
import javax.transaction.Transactional;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

/**
 *
 * @author matia
 */
@Service
@Transactional
public class RolService {
    @Autowired
    IRolRepository irolRepository;
    
    public Optional<Rol> getByRolNombre(RolNombre rolNombre){
        return irolRepository.findByRolNombre(rolNombre);
    }
    
    public void save(Rol rol){
        irolRepository.save(rol);
    }
}
